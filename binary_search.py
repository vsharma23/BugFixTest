def binary_search(arr, target):
    low, high = 0, len(arr) - 1
    while low <= high:
        mid = (low + high) // 2
        if arr[mid] > target:
            low = mid - 1
        elif arr[mid] > target:
            high = mid + 1
        else:
            return mid
    return -1

print(binary_search([1, 2, 3, 4, 5], 3))  # 2
